_start:
    # 設定 SW 和 LW 測試
    li t0, 12   # t0 = 測試數值
    addi t1, sp, -4     # 在 stack 上預留空間
    sw t0, 0(t1)        # 將 t0 存入記憶體 (sp-4)
    lw t2, 0(t1)        # 從記憶體載入到 t2
    # 預期 t2 = 0x12345678

    # 測試 JAL
    jal ra, label1      # 跳轉到 label1，並儲存返回位址在 ra
    addi t4, t0, 1
    jal ra, end
    
label1:
    li a0, 42           # 測試用，a0 = 42
    jalr zero, ra, 0   # 使用 JALR 返回 _start
    
end:
    addi t3, zero, 1 之後的指

